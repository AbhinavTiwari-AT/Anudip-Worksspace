package com.anudip.javaDemo;

public class switchcase {
	
	public static void main(String[] args) {
	    char grade='A';
	    
	    switch(grade)
	    {
	    case 'A':
	    	System.out.println("grade A:Exillent");	
	    	break;
	    	
	    case 'B':
	    	System.out.println("grade A:v good");	
	    	break;
	    	
	    case 'C':
	    	System.out.println("grade A:good");	
	    	break;
	    	
	    	
	    default: 
	    	System.out.println("Wrong input");
	    	
	    }
	    
	   
	}

}
